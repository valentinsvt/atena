    public void displayRegistro() {
        if (validacion) {
            RegistroBio reg = new RegistroBio();
            panelPrincipal.removeAll();
            panelPrincipal.add(reg);
            validate();
            reg.setFplib(fplib);
            reg.setDeviveInfo(deviceInfo);
            reg.setPadre(this);
            reg.validate();
            reg.setLocation(0, 0);
            reg.setSize(700, 700);
            reg.setVisible(true);
            panelPrincipal.repaint();
        }
    }
     public void displayControl() {
        if (validacion) {
            ControlAcceso control = new ControlAcceso();
            panelPrincipal.removeAll();
            panelPrincipal.add(control);
            validate();
            control.setFplib(fplib);
            control.setDeviveInfo(deviceInfo);
            control.setPadre(this);
            control.validate();
            control.setLocation(0, 0);
            control.setSize(700, 700);
            control.setVisible(true);
            panelPrincipal.repaint();
        }
    }
